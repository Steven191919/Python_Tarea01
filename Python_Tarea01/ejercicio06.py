""" Calcular el área y el perímetro de un Rectángulo"""

alto=int(float(input ("Ingresa el alto del rectangulo: ")))
ancho=int(float(input("ingresa el ancho del rectangulo: ")))
Area= alto * ancho
Perimetro=(alto + ancho)*2
print(f"El area es: {Area}cm \n El perimetro es: {Perimetro}cm")


